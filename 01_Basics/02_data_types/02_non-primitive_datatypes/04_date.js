// Represent specific points in time.
// Created using the Date() constructor.
// Example:

 const now = new Date();

 console.log(now) // current date and time
 console.log(now.getDate()) // get current date only
 console.log(now.getFullYear()) // get current year only
 console.log(now.getMinutes()) // get minutes passed
 

