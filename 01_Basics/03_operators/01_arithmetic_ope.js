// Arithmetic Operators are used to perform arithmetic on numbers:

// 01: Addition ( + )
console.log(200 + 300) // 500


//  02: Subtraction ( - )
console.log(400 - 100) // 300



// 03 :  Multiplication ( * )
console.log(10 * 6) // 60



// 04:  Division ( / )
console.log(30 / 3 ) // 10



// 05:  Modulues or Reminder ( % )
console.log(50 % 4 ) // 2




// 06:  Exponent ( ** )
console.log(2**3) // 2 power 3 mean  2 x 2 x 2 = 2



// 07:  increement ( ++ )
let a = 10
a++
console.log(a) // 11



// 08:   Decrement ( --)
let b = 20
b--
console.log(b) // 19


/// 

