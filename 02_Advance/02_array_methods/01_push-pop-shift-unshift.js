let colors = ["black", "blue", "green", "orange", "yellow"]
console.log(colors)

//// pop()  method: remove last element and make changes into original array
colors.pop() 
console.log(colors) // yellow remove : return a new array


//// push("red", "pink") method: add new element at the last position of array
colors.push("red", "pink") // red , pink
console.log(colors) // added red , pink and returns new array


//// shift() method:  remove first element from array
colors.shift()  // black remove
console.log(colors) // return new array


// unshift("brown", "purple") : add new element at the first position of the array
colors.unshift("brown", "purple")
console.log(colors) // returns new array








